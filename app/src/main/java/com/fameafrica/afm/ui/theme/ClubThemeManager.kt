    fun getThemeForTeam(teamName: String): ClubThemeConfig {
        val palette = clubPalettes[teamName] ?: return DefaultConfig
        return createThemeConfig(
            id = 0,
            primaryHex = palette.first,
            secondaryHex = palette.second,
            accentHex = palette.first // Using primary as accent for now
        )
    }

    fun getColorsForTeam(teamName: String): List<String> {
        val palette = clubPalettes[teamName] ?: return listOf("#1B5E20")
        return listOf(palette.first, palette.second)
    }

    fun getThemeForThemePreset(preset: FootballThemePreset): ClubThemeConfig {
        return when (preset) {
            FootballThemePreset.CHAIRMAN_MODE -> ClubThemeConfig(
                clubId = -2,
                primaryColor = FameColors.DeepNavyBlack,
                secondaryColor = FameColors.AmberBronze,
                accentColor = FameColors.ChampionsGold
            )
            else -> DefaultConfig
        }
    }

    /**
     * Converts raw data (from DB or API) into a Theme Config
     */
    fun createThemeConfig(
        id: Int, 
        primaryHex: String, 
        secondaryHex: String, 
        accentHex: String
    ): ClubThemeConfig {
        return ClubThemeConfig(
            clubId = id,
            primaryColor = safeParseColor(primaryHex),
            secondaryColor = safeParseColor(secondaryHex),
            accentColor = safeParseColor(accentHex)
        )
    }

    private fun safeParseColor(hex: String): Color {
        return try {
            Color(hex.toColorInt())
        } catch (e: Exception) {
            Color.Gray
        }
    }

    /**
     * Generates a Material3 ColorScheme based on the club config
     */
    fun generateColorScheme(config: ClubThemeConfig?): ColorScheme {
        val theme = config ?: DefaultConfig
        
        return darkColorScheme(
            primary = theme.primaryColor,
            secondary = theme.secondaryColor,
            tertiary = theme.accentColor,
            surface = Color(0xFF121212), // Neutral dark surface
            background = theme.primaryColor.copy(alpha = 0.1f), // Tinted background
            onPrimary = if (isColorLight(theme.primaryColor)) Color.Black else Color.White,
            onSecondary = if (isColorLight(theme.secondaryColor)) Color.Black else Color.White,
            onSurface = Color.White,
            onBackground = Color.White
        )
    }

    private fun isColorLight(color: Color): Boolean {
        val argb = color.toArgb()
        val r = android.graphics.Color.red(argb)
        val g = android.graphics.Color.green(argb)
        val b = android.graphics.Color.blue(argb)
        val luminance = (0.299 * r + 0.587 * g + 0.114 * b) / 255
        return luminance > 0.5
    }
